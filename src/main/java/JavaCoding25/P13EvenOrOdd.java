package JavaCoding25;

public class P13EvenOrOdd {

    public static void main(String[] args) {

        evenOrOdd(100);
    }

    public static void evenOrOdd(int num) {

        if(num%2==0) {

            System.out.println("even");

        }
        else {
            System.out.println("odd");
        }
    }
}
